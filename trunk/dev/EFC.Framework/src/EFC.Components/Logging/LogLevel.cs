namespace EFC.Components.Logging
{
    /// <summary>
    /// The LogLevel enum.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// The All level.
        /// </summary>
        All,

        /// <summary>
        /// The trace level.
        /// </summary>
        Trace,

        /// <summary>
        /// The debug level.
        /// </summary>
        Debug,

        /// <summary>
        /// The information level.
        /// </summary>
        Information,

        /// <summary>
        /// The warning level.
        /// </summary>
        Warning,

        /// <summary>
        /// The error level.
        /// </summary>
        Error,

        /// <summary>
        /// The fatal error level.
        /// </summary>
        Fatal,

        /// <summary>
        /// The Off level.
        /// </summary>
        Off
    }
}
